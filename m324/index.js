// Objects

// describe a peice of data in depth

// key: value pairs
var dog = {
    name: "spot",
    age: 4,
    isHungry: true,
    favToys: ["bone", "ball", "squeeky thing"]
}

// dot notation
console.log(dog.age)

// bracket notation
console.log(dog["age"])

var larry = {
    name: "larry",
    age: 20,
    friends: ["john", "lisa", "al"],
    isPetOwner: true
}

console.log(larry.friends)